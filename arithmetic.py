'''
Arithmetic operators

+  addition
-  subtraction
/  division
*  multiplication
%  modulus
** exponent
//  floor division 

'''
a = 35
b = 4
print("Addition : " , (a+b))
print("Subtraction : " , (a-b))
print("Multiplication : " , (a*b))
print("Division : " , (a/b))
print("modulus or remainder : " , (a%b))
print("exponent : " , (a**b))
print("floor division : " , (a//b))
